//==========================================================
//	九九表を表示する
//==========================================================
#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h> // printf()

int main()
{
	// ここをコーディングしてください。
	puts("    |   1   2   3   4   5   6   7   8   9");
	puts("----+------------------------------------");
	for (int i = 1; i <= 9; i++) {
		printf("%4d|", i);
		for (int j = 1; j <= 9; j++) {
			printf("%4d", j*i);
		}
		puts("");
	}
	return 0;
}